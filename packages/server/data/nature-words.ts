/**
 * Count nature/imagery words in text.
 * Returns count of nature words found (higher = more nature imagery).
 */
export function countNatureWords(text: string): number {
  const words = text.toLowerCase().split(/\s+/);
  return words.filter((word) => NATURE_WORDS.has(word)).length;
}
